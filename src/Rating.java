public class Rating 
{
    private int Stars;
    private String Comments;

    public int getStars() 
    {
        return Stars;
    }
    public String getComments()
    {
        return Comments;
    }
    public void setComments(String Comments) 
    {
        this.Comments = Comments;
    }
    public void setStars(int Stars) 
    {
        this.Stars = Stars;
    }
}
